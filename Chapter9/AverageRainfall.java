
public class AverageRainfall{
    public static void main (String[] args)
    {
        double april = 12;
        double june = 8;
        double may = 14;
        double average = 0;

        average = (april + june + may) / 3;


        System.out.println("Average for April is " + april);
        System.out.println("Average for June is " + june);
        System.out.println("Average for May is " + may);
        System.out.println("The average for the three is " + average);

        
    }
}
